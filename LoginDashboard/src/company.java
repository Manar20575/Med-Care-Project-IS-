    private void add_dealActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_dealActionPerformed
        // TODO add your handling code here: 
//  ==============================>>>> why      if(total_amount_deals_input.getText().equals(true)){
//            JOptionPane.showMessageDialog(this,"Total Ammount nt input by UU its option show in (search botton)");
//        }
        if(companyname_input.getText().equals("") || companyphone_input.getText().equals("") || companyaddress_input.getText().equals("") || med_name_input.getText().equals("") || med_amount_input.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Please, Add all data!");
        }
        else{
            String data[] = { companyname_input.getText(),companyphone_input.getText(), companyaddress_input.getText(),med_name_input.getText(),med_amount_input.getText()};
            DefaultTableModel tblModel = (DefaultTableModel)company_table.getModel();
            tblModel.addRow(data);
            try {
                PreparedStatement check = con.prepareStatement("select m_name from manufactors where m_name=?");
                check.setString(1, companyname_input.getText());
                ResultSet rs = check.executeQuery();
                if(!rs.next())
                {
                    PreparedStatement stm = con.prepareStatement("insert into manufactors (m_name,m_number, m_address) values (?, ?, ?)");
                    stm.setString(1, companyname_input.getText());
                    stm.setInt(2, (int) Double.parseDouble(companyphone_input.getText()));
                    stm.setString(3, companyaddress_input.getText());
                    stm.executeUpdate();
                }
                PreparedStatement stm2 = con.prepareStatement("insert into drugs (drug_name, amount, m_name, drug_price) values (?, ?, ?, ?)");
                stm2.setString(3, companyname_input.getText());
                stm2.setString(1, med_name_input.getText());
                stm2.setInt(2, (int) Double.parseDouble(med_amount_input.getText()));
                stm2.setInt(4, (int) Double.parseDouble(med_price_input.getText()));
                stm2.executeUpdate();
                JOptionPane.showMessageDialog(this,"Added Successfully!");
                fillTable();
            } catch (SQLException ex) {
                Logger.getLogger(attendance.class.getName()).log(Level.SEVERE, null, ex);
            }
            companyname_input.setText("");
            companyphone_input.setText("");
            companyaddress_input.setText("");
            med_name_input.setText("");
            med_amount_input.setText("");
            }
    }//GEN-LAST:event_add_dealActionPerformed
    private void fillTable() {
        try {
            dtm.setRowCount(0);
            PreparedStatement stm = con.prepareStatement("select drugs.m_name, manufactors.m_number, manufactors.m_address,drugs.drug_name, drugs.drug_price, drugs.amount from manufactors, drugs where drugs.m_name = manufactors.m_name");
            ResultSet rs = stm.executeQuery();
            while(rs.next())
            {
                dtm.addRow(new Object[]{rs.getString(1),rs.getInt(2),rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6)});  
            }
            company_table.setModel(dtm);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }
